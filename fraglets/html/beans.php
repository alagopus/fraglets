<P>Here is the first contribution, a simple bean to edit integer values.
The Applet below is a showcase for that bean. You may even add any
other component to the bean creation list with "New Bean" from the
file menu.</P>

<P>Your browser will need Swing 1.1 installed for the java engine to
be able to run this applet. The easiest way is to install a Java 2
release from sun, these include the <a
href="http://java.sun.com/products/jdk/1.2/jre/" >java plugin<a>.</P>

